void prime_fact(void)
{
    int i,n,sum=0,j,v;
    for(i=1; i<=1000; i++)
    {
        n=i;
        j=0;
        sum=1;
        while(primes[j]*primes[j]<=i)
        {
            if(n%primes[j]==0)
            {
                v=1;
                while(n%primes[j]==0)
                {
                    v=v*primes[j]+1;
                    n=n/primes[j];
                }
                sum=sum*v;
            }
            j++;
        }
        if(n>1)
        {
            sum=sum*(n+1);
        }
        if(sum<1000)
            output[sum]=i;
    }
}
